#!/usr/bin/env node

/**
 * AI Lab Integrity Guardian
 *
 * Validates app/ai-lab tool catalog integrity:
 * - Every tool href in app/ai-lab/ai-lab-tools.ts has a corresponding page.tsx
 * - All missing pages are reported to automation/reports/ai-lab-integrity-latest.json
 *
 * Env:
 *   CONTINUOUS_MODE=true
 *   INTERVAL_MINUTES=120
 *   AUTO_REMEDIATE=1            # Create missing page scaffolds
 *   AUTO_OPEN_ISSUE=1           # Open deduped GitHub issue when missing persists
 *   AUTO_PAGES_VISIT_BACKFILL=1 # Merge AI Lab hrefs into pages-to-visit.json (aiLab)
 *   AUTO_SMOKE_ROUTES_SYNC=1    # After remediation, regenerate config/smoke-routes.txt
 */

const fs = require('fs');
const path = require('path');
const { execFileSync } = require('child_process');
const { syncAiLabHrefs } = require('./lib/pages-to-visit-backfill.cjs');

const ROOT = process.cwd();
const TOOLS_PATH = path.join(ROOT, 'app', 'ai-lab', 'ai-lab-tools.ts');
const REPORT_PATH = path.join(ROOT, 'automation', 'reports', 'ai-lab-integrity-latest.json');
const CONTINUOUS = process.env.CONTINUOUS_MODE === 'true' || process.env.CONTINUOUS_MODE === '1';
const INTERVAL_MINUTES = Math.max(15, parseInt(process.env.INTERVAL_MINUTES || '120', 10));
const AUTO_REMEDIATE = process.env.AUTO_REMEDIATE === '1';
const AUTO_OPEN_ISSUE = process.env.AUTO_OPEN_ISSUE === '1';
const AUTO_PAGES_VISIT_BACKFILL = process.env.AUTO_PAGES_VISIT_BACKFILL !== '0';
const AUTO_SMOKE_ROUTES_SYNC = process.env.AUTO_SMOKE_ROUTES_SYNC !== '0';
const SMOKE_ROUTES_GEN = path.join(ROOT, 'scripts', 'automation', 'generate-smoke-routes.cjs');
const ISSUE_SCRIPT = path.join(ROOT, 'scripts', 'automation', 'gh-issue-dedupe-or-create.cjs');

function log(msg) {
  console.log(`[AILabIntegrity] ${new Date().toISOString()} | ${msg}`);
}

function ensureDir(p) {
  const d = path.dirname(p);
  if (!fs.existsSync(d)) fs.mkdirSync(d, { recursive: true });
}

function parseToolHrefs(ts) {
  const hrefs = [];
  for (const m of ts.matchAll(/href:\s*'([^']+)'/g)) {
    const href = m[1];
    if (href.startsWith('/ai-lab/') && href !== '/ai-lab') hrefs.push(href);
  }
  return [...new Set(hrefs)].sort();
}

function toTitleFromHref(href) {
  const slug = href.replace(/^\/ai-lab\//, '');
  return slug
    .split(/[-/]/)
    .filter(Boolean)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(' ');
}

function toComponentNameFromHref(href) {
  const slug = href.replace(/^\/ai-lab\//, '');
  const parts = slug.split(/[-/]/).filter(Boolean);
  let name = parts
    .map((p) => p.charAt(0).toUpperCase() + p.slice(1).replace(/[^a-zA-Z0-9]/g, ''))
    .join('');
  if (!name) name = 'AiLabTool';
  if (!/^[A-Za-z]/.test(name)) name = `Tool${name}`;
  return `${name}Page`;
}

function layoutImportPath(rel) {
  const depth = rel.split('/').filter(Boolean).length;
  return `${'../'.repeat(depth + 1)}components/ai-lab/AILabToolLayout`;
}

function scaffoldPage(href, pagePath) {
  const title = toTitleFromHref(href);
  const rel = href.replace(/^\/ai-lab\//, '');
  const componentName = toComponentNameFromHref(href);
  const importPath = layoutImportPath(rel);
  const desc =
    'AI Lab tool — autonomous scaffold from integrity guardian. Add inputs, scoring, and data wiring.';
  const sub =
    'This scaffold was generated by the AI Lab integrity guardian. Replace placeholders with domain logic, API calls, and scoring.';
  const body = `import type { Metadata } from 'next';
import { AILabToolLayout } from '${importPath}';

export const metadata: Metadata = {
  title: ${JSON.stringify(title)},
  description: ${JSON.stringify(desc)},
};

export default function ${componentName}() {
  return (
    <div className="bg-slate-950/90">
      <AILabToolLayout
        title={${JSON.stringify(title)}}
        subtitle={${JSON.stringify(sub)}}
      >
        <div className="space-y-8">
          <section className="grid gap-4 md:grid-cols-3">
            <div className="rounded-2xl border border-slate-800/80 bg-slate-900/80 p-5 md:col-span-2">
              <p className="text-xs font-semibold uppercase tracking-wide text-slate-300">Inputs</p>
              <p className="mt-2 text-sm text-slate-400">
                Define prompts, files, or parameters users provide. Wire to client state or server actions.
              </p>
              <div className="mt-4 rounded-xl border border-dashed border-slate-700 bg-slate-950/60 p-4 text-xs text-slate-500">
                Placeholder input area
              </div>
            </div>
            <div className="rounded-2xl border border-slate-800/80 bg-slate-900/80 p-5">
              <p className="text-xs font-semibold uppercase tracking-wide text-slate-300">Score</p>
              <p className="mt-2 text-4xl font-bold text-emerald-300">
                —<span className="ml-1 text-base font-semibold text-slate-400">/100</span>
              </p>
              <p className="mt-2 text-xs text-slate-500">Compute from rubric or model output.</p>
            </div>
          </section>

          <section className="rounded-2xl border border-slate-800/80 bg-slate-900/80 p-5">
            <p className="text-xs font-semibold uppercase tracking-wide text-slate-300">Actions</p>
            <div className="mt-4 flex flex-wrap gap-3">
              <button
                type="button"
                className="rounded-full border border-sky-500/50 bg-sky-500/10 px-4 py-2 text-xs font-semibold text-sky-100"
              >
                Run analysis
              </button>
              <button
                type="button"
                className="rounded-full border border-slate-600 px-4 py-2 text-xs font-semibold text-slate-200"
              >
                Export summary
              </button>
            </div>
          </section>
        </div>
      </AILabToolLayout>
    </div>
  );
}
`;
  fs.mkdirSync(path.dirname(pagePath), { recursive: true });
  fs.writeFileSync(pagePath, body);
}

function openIssueForMissing(missing, remediatedCount) {
  if (!fs.existsSync(ISSUE_SCRIPT)) return;
  const title = '[automation] AI Lab integrity missing route pages';
  const details = missing
    .map((m) => `- \`${m.href}\` -> \`${m.expectedPagePath}\``)
    .join('\n');
  const body = [
    'AI Lab integrity guardian detected missing route implementations.',
    '',
    `Missing count: ${missing.length}`,
    `Auto-remediated in this run: ${remediatedCount}`,
    '',
    '## Missing routes',
    details,
    '',
    `Dedupe key: ai-lab-integrity:${missing.map((m) => m.href).sort().join('|')}`,
  ].join('\n');
  try {
    execFileSync('node', [ISSUE_SCRIPT, title, body, '--label', 'automation', '--label', 'ai-lab', '--fingerprint', 'ai-lab-integrity-missing-pages'], {
      cwd: ROOT,
      stdio: 'ignore',
    });
  } catch (_) {}
}

function checkOnce() {
  ensureDir(REPORT_PATH);
  if (!fs.existsSync(TOOLS_PATH)) {
    const report = { ok: false, error: 'missing_tools_file', at: new Date().toISOString() };
    fs.writeFileSync(REPORT_PATH, JSON.stringify(report, null, 2));
    return report;
  }

  const toolsRaw = fs.readFileSync(TOOLS_PATH, 'utf8');
  const hrefs = parseToolHrefs(toolsRaw);
  const missing = [];
  const healthy = [];

  let remediatedCount = 0;
  for (const href of hrefs) {
    const rel = href.replace(/^\/ai-lab\//, '');
    const pagePath = path.join(ROOT, 'app', 'ai-lab', rel, 'page.tsx');
    if (fs.existsSync(pagePath)) {
      healthy.push(href);
    } else {
      if (AUTO_REMEDIATE) {
        scaffoldPage(href, pagePath);
        remediatedCount += 1;
      }
      missing.push({ href, expectedPagePath: path.relative(ROOT, pagePath) });
    }
  }

  if (AUTO_REMEDIATE && remediatedCount > 0) {
    healthy.push(...missing.slice(0, remediatedCount).map((m) => m.href));
  }

  const remainingMissing = AUTO_REMEDIATE
    ? missing.filter((m) => !fs.existsSync(path.join(ROOT, m.expectedPagePath)))
    : missing;

  let pagesVisitBackfill = { added: 0, addedPaths: [], skipped: true };
  if (AUTO_PAGES_VISIT_BACKFILL) {
    pagesVisitBackfill = syncAiLabHrefs({ hrefs });
  }

  let smokeRoutesSynced = false;
  if (remediatedCount > 0 && AUTO_SMOKE_ROUTES_SYNC && fs.existsSync(SMOKE_ROUTES_GEN)) {
    try {
      execFileSync(process.execPath, [SMOKE_ROUTES_GEN], { cwd: ROOT, stdio: 'ignore' });
      smokeRoutesSynced = true;
    } catch (e) {
      log(`warn: smoke routes sync failed: ${e.message}`);
    }
  }

  const report = {
    at: new Date().toISOString(),
    ok: remainingMissing.length === 0,
    totalTools: hrefs.length,
    healthy: hrefs.length - remainingMissing.length,
    missingCount: remainingMissing.length,
    remediatedCount,
    missing: remainingMissing,
    autoRemediate: AUTO_REMEDIATE,
    autoOpenIssue: AUTO_OPEN_ISSUE,
    autoPagesVisitBackfill: AUTO_PAGES_VISIT_BACKFILL,
    pagesVisitBackfill,
    autoSmokeRoutesSync: AUTO_SMOKE_ROUTES_SYNC,
    smokeRoutesSynced,
  };
  fs.writeFileSync(REPORT_PATH, JSON.stringify(report, null, 2));
  if (AUTO_OPEN_ISSUE && remainingMissing.length > 0) {
    openIssueForMissing(remainingMissing, remediatedCount);
  }
  log(
    `ok=${report.ok} healthy=${report.healthy}/${report.totalTools} missing=${report.missingCount} remediated=${remediatedCount}`
  );
  return report;
}

async function main() {
  if (!CONTINUOUS) {
    const r = checkOnce();
    process.exit(r.ok ? 0 : 1);
  }

  log(`Continuous mode: every ${INTERVAL_MINUTES} minutes`);
  for (;;) {
    checkOnce();
    await new Promise((r) => setTimeout(r, INTERVAL_MINUTES * 60 * 1000));
  }
}

main().catch((e) => {
  log(`Fatal: ${e.message}`);
  process.exit(1);
});
