#!/usr/bin/env node
/**
 * get-workflow-logs.cjs – Fetch recent GitHub Actions workflow runs and generate a summary.
 *
 * Requirements:
 *   - Node >=18
 *   - @octokit/rest (installed as dependency)
 *
 * This script fetches the latest 10 workflow runs, extracts key metrics,
 * and outputs a Markdown report to STDOUT. Can be used as:
 *   • CI step to generate daily report
 *   • Input to Telegram alerting workflow
 *   • Daily health-check for CI pipeline
 *
 * Environment Variables (via .env or CI secrets):
 *   GITHUB_TOKEN – Personal access token with repo scope
 *   REPO_OWNER   – Owner of the repository (e.g., "Zion-support")
 *   REPO_NAME    – Repository name (e.g., "zion.app")
 *
 * Usage:
 *   REPO_OWNER=Zion-support REPO_NAME=zion.app GITHUB_TOKEN=ghp_xxxx node scripts/get-workflow-logs.cjs > reports/daily-report.md
 *
 * Output Format (Markdown):
 *   # CI Health Report - YYYY-MM-DD
 *   - **Total Runs:** 10
 *   - **Passed:** 8
 *   - **Failed:** 2
 *   - **Most Recent Run:** {name} ({conclusion})
 *   - **Avg. Duration:** 4.2 min
 *
 * For Telegram integration: pipe output to `scripts/send-telegram-summary.cjs`
 */

require('dotenv').config();
const { Octokit } = require('@octokit/rest');

const {
  GITHUB_TOKEN,
  REPO_OWNER,
  REPO_NAME
} = process.env;

if (!GITHUB_TOKEN || !REPO_OWNER || !REPO_NAME) {
  console.error('❌ Missing required env vars: GITHUB_TOKEN, REPO_OWNER, REPO_NAME');
  process.exit(1);
}

const octokit = new Octokit({ auth: GITHUB_TOKEN });

async function fetchRecentRuns() {
  try {
    console.log(`🔍 Fetching workflow runs for ${REPO_OWNER}/${REPO_NAME}...`);
    const { data } = await octokit.actions.listWorkflowRuns({
      owner: REPO_OWNER,
      repo: REPO_NAME,
      per_page: 10
    });
    console.log(`✅ Successfully fetched ${data.total_count} workflow runs`);
    return data.workflow_runs;
  } catch (err) {
    console.error('❌ Failed to fetch workflow runs:', err.message);
    process.exit(1);
  }
}

function generateMarkdownReport(runs) {
  const passed = runs.filter(r => r.conclusion === 'success').length;
  const failed = runs.filter(r => r.conclusion === 'failure').length;
  const mostRecent = runs[0] || {};
  const avgDuration = runs.reduce((sum, r) => sum + (r.duration || 0), 0) / runs.length / 60000; // minutes

  const report = `# CI Health Report - ${new Date().toISOString().split('T')[0]}\n\n`;
  report += `*Total Runs:* ${runs.length}\n`;
  report += `*Passed:* ${passed}\n`;
  report += `*Failed:* ${failed}\n\n`;
  report += `*Most Recent Run:* **${mostRecent.name}**\n`;
  report += `  *Conclusion:* ${mostRecent.conclusion}\n`;
  report += `  *Duration:* ${mostRecent.duration ? (mostRecent.duration / 60000).toFixed(2) + ' min' : 'N/A'}\n\n`;
  report += `*Average Duration:* ${avgDuration.toFixed(2)} min\n\n`;
  report += `Generated by \`get-workflow-logs.cjs\` on ${new Date().toISOString().split('T')[0]}\n`;

  return report;
}

async function main() {
  const runs = await fetchRecentRuns();
  const report = generateMarkdownReport(runs);
  console.log(report);
}

main().catch(err => {
  console.error('❌ Unexpected error:', err.message);
  process.exit(1);
});
