#!/usr/bin/env node
 
const fs = require('fs');
const path = require('path');
const glob = require('glob');

const ROOT = process.cwd();
const APP_DIR = path.join(ROOT, 'app');
const OUTPUT_FILE = path.join(ROOT, 'config', 'smoke-routes.txt');
const CHECK_MODE = process.argv.includes('--check');
const DEFAULT_MAX_ROUTES = Number(process.env.SMOKE_MAX_ROUTES || 60);

function loadCatalogAiLabHrefs() {
  const toolsPath = path.join(ROOT, 'app', 'ai-lab', 'ai-lab-tools.ts');
  if (!fs.existsSync(toolsPath)) return new Set();
  const text = fs.readFileSync(toolsPath, 'utf8');
  const hrefs = new Set();
  const re = /href:\s*'([^']+)'/g;
  let m;
  while ((m = re.exec(text))) {
    if (m[1].startsWith('/ai-lab/')) hrefs.add(m[1]);
  }
  return hrefs;
}

const PRIORITY_ROUTES = [
  '/',
  '/about',
  '/automation',
  '/blog',
  '/contact',
  '/services',
  '/solutions',
  '/ai-lab',
  '/zion-ai-chatbot-playground',
  '/zion-ai-code-sandbox',
  '/zion-ai-site-evolution-simulator',
];

function toRoute(filePath) {
  const normalized = filePath.replace(/\\/g, '/');
  const rel = normalized
    .replace(/^app\//, '')
    .replace(/(^|\/)page\.tsx$/, '');
  if (!rel) {
    return '/';
  }
  return `/${rel}`;
}

function isStaticRoute(routePath) {
  if (!routePath.startsWith('/')) return false;
  if (routePath.includes('[') || routePath.includes(']')) return false;
  if (routePath.startsWith('/api')) return false;
  return true;
}

function buildRoutes() {
  const catalog = loadCatalogAiLabHrefs();
  const files = glob.sync('app/**/page.tsx', {
    cwd: ROOT,
    nodir: true,
  });

  const routes = new Set();
  for (const file of files) {
    const routePath = toRoute(file);
    if (!isStaticRoute(routePath)) continue;
    routes.add(routePath);
  }

  const all = Array.from(routes).sort((a, b) => a.localeCompare(b));
  const selected = [];
  const seen = new Set();

  const add = (routePath) => {
    if (!routes.has(routePath) || seen.has(routePath)) return;
    selected.push(routePath);
    seen.add(routePath);
  };

  for (const routePath of PRIORITY_ROUTES) {
    add(routePath);
  }

  for (const routePath of [...catalog].sort((a, b) => a.localeCompare(b))) {
    add(routePath);
  }

  for (const routePath of all) {
    if (routePath.startsWith('/ai-lab/') && !catalog.has(routePath)) {
      add(routePath);
    }
  }

  for (const routePath of all) {
    add(routePath);
  }

  const mandatory = new Set();
  for (const routePath of PRIORITY_ROUTES) {
    if (routes.has(routePath)) mandatory.add(routePath);
  }
  for (const routePath of catalog) {
    if (routes.has(routePath)) mandatory.add(routePath);
  }
  const cap = Math.max(DEFAULT_MAX_ROUTES, mandatory.size);
  return selected.slice(0, cap);
}

function buildContent(routes) {
  return [
    '# Auto-generated by scripts/automation/generate-smoke-routes.cjs',
    '# Source: app/**/page.tsx (static routes only)',
    `# Routes: ${routes.length} (SMOKE_MAX_ROUTES default ${DEFAULT_MAX_ROUTES}; expanded if catalog+priority exceed cap)`,
    ...routes,
    '',
  ].join('\n');
}

function parseRoutesFromContent(content) {
  return content
    .split('\n')
    .map((line) => line.trim())
    .filter((line) => line && !line.startsWith('#'));
}

function main() {
  if (!fs.existsSync(APP_DIR)) {
    console.error('app/ directory not found.');
    process.exit(1);
  }

  const routes = buildRoutes();
  const nextContent = buildContent(routes);
  const prevContent = fs.existsSync(OUTPUT_FILE)
    ? fs.readFileSync(OUTPUT_FILE, 'utf8')
    : '';

  if (CHECK_MODE) {
    const prevRoutes = parseRoutesFromContent(prevContent);
    const nextRoutes = parseRoutesFromContent(nextContent);
    if (prevRoutes.join('\n') !== nextRoutes.join('\n')) {
      const prevSet = new Set(prevRoutes);
      const nextSet = new Set(nextRoutes);
      const missing = prevRoutes.filter((r) => !nextSet.has(r));
      const added = nextRoutes.filter((r) => !prevSet.has(r));

      console.error('Smoke routes are out of date. Run: npm run smoke:routes:generate');
      console.error(
        JSON.stringify(
          {
            prevCount: prevRoutes.length,
            nextCount: nextRoutes.length,
            missing: missing.slice(0, 10),
            added: added.slice(0, 10),
            missingMore: Math.max(0, missing.length - 10),
            addedMore: Math.max(0, added.length - 10),
          },
          null,
          2,
        ),
      );
      process.exit(1);
    }
    console.log(`Smoke routes check passed (${routes.length} routes).`);
    return;
  }

  fs.mkdirSync(path.dirname(OUTPUT_FILE), { recursive: true });
  fs.writeFileSync(OUTPUT_FILE, nextContent, 'utf8');
  console.log(`Generated ${OUTPUT_FILE} with ${routes.length} routes.`);
}

main();
